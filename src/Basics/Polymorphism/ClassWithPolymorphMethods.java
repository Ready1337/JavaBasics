package Basics.Polymorphism;

public class ClassWithPolymorphMethods {

    public void printSomeThing() {
        System.out.println("I am printSomeThing() method:\nsomeThing");
    }

    public void printSomeThing(String anotherThing) {
        System.out.println("I am printSomeThing(String anotherThing) method:\nsomeThing " + anotherThing);
    }
}
