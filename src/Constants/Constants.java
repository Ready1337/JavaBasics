package Constants;

public class Constants {

    public static void main(String[] Args){
        /*
        Константы в Java: https://vertex-academy.com/tutorials/ru/konstanty-v-java/
         */

        /*
        Константы принято объявлять с большой буквы
        Объявление констант: final <тип> <название>;
         */
        final int ExampleOfIntConstantVariable = 1;

        System.out.println(ExampleOfIntConstantVariable + 1);
    }
}
